'use strict';

import mongoose from 'mongoose';

const UserBlogSchema = new mongoose.Schema({
    userId: {
        type: String,
        required: true
    },
    displayName: {
        type: String,
        required: true
    },
    firstName: {
        type: String,
        required: true
    },
    lastName: {
        type: String,
        required: true
    },
    image: {
        type: String
    },
    createdAt: {
        type: Date,
        default: Date.now
    }
});
export default mongoose.model('UserBlog', UserBlogSchema);
